package com.example.mymusicapp.API;

public class Song {
    private int id;
    private String title;
    private String singer;
    private String url;
    private String album;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getSinger() {
        return singer;
    }

    public String getUrl() {
        return url;
    }

    public String getAlbum() {
        return album;
    }

    public Song(){
    }
}
